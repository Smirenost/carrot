/**
 * The activation type.
 */
export enum ActivationType {
    /**
     * No activation used for exceptions.
     */
    NO_ACTIVATION,
    /**
     * Logistic activation.
     */
    LogisticActivation,
    /**
     * Tanh activation.
     */
    TanhActivation,
    /**
     * Identity activation.
     */
    IdentityActivation,
    /**
     * Step activation.
     */
    StepActivation,
    /**
     * RELU activation.
     */
    RELUActivation,
    /**
     * SoftSign activation.
     */
    SoftSignActivation,
    /**
     * Sinusoid activation.
     */
    SinusoidActivation,
    /**
     * Gaussian activation.
     */
    GaussianActivation,
    /**
     * BentIdentity activation.
     */
    BentIdentityActivation,
    /**
     * Bipolar activation.
     */
    BipolarActivation,
    /**
     * BipolarSigmoid activation.
     */
    BipolarSigmoidActivation,
    /**
     * HardTanh activation.
     */
    HardTanhActivation,
    /**
     * Absolute activation.
     */
    AbsoluteActivation,
    /**
     * Inverse activation.
     */
    InverseActivation,
    /**
     * SELU activation.
     */
    SELUActivation,
    /**
     * MISH activation.
     */
    MISHActivation,
}
